/* NSCode codegen engine — deterministic, offline code generation.
 * Given (kind, spec) produce consistent code in 4 formats:
 * JSON / YAML / Python / LangGraph. All reflect the spec fields.
 * Pure functions, no DOM, no globals beyond NSCode.codegen. */
(function (NSCode) {
  'use strict';

  /* ---------- helpers ---------- */
  function isArray(v) { return Object.prototype.toString.call(v) === '[object Array]'; }
  function isObject(v) { return v && typeof v === 'object' && !isArray(v); }
  function repeat(s, n) { var o = ''; for (var i = 0; i < n; i++) o += s; return o; }

  /* snake_case-ish identifier from a free-text name */
  function ident(s, fallback) {
    s = String(s == null ? '' : s).trim().toLowerCase()
      .replace(/[^a-z0-9]+/g, '_').replace(/^_+|_+$/g, '');
    if (!s) s = fallback || 'x';
    if (/^[0-9]/.test(s)) s = '_' + s;
    return s;
  }

  /* ---------- JSON ---------- */
  function toJSON(spec) {
    return JSON.stringify(spec, null, 2);
  }

  /* ---------- YAML ---------- */
  function yamlScalar(v) {
    if (v === null || typeof v === 'undefined') return 'null';
    if (typeof v === 'boolean') return v ? 'true' : 'false';
    if (typeof v === 'number') return String(v);
    var s = String(v);
    if (s === '') return '""';
    // Quote if it could be misparsed (special chars, leading/trailing space,
    // reserved words, looks like number/bool/null).
    if (/[:#\[\]\{\}&\*!\|>'"%@`,]/.test(s) ||
        /^[\s]|[\s]$/.test(s) ||
        /^(true|false|null|yes|no|on|off|~)$/i.test(s) ||
        /^[-?]/.test(s) ||
        /^[0-9.+-]/.test(s) && /^-?\d+(\.\d+)?$/.test(s) ||
        s.indexOf('\n') !== -1) {
      return '"' + s.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n') + '"';
    }
    return s;
  }

  function yamlNode(v, indent) {
    var pad = repeat('  ', indent);
    var lines = [];
    if (isArray(v)) {
      if (!v.length) return ' []';
      var out = '\n';
      v.forEach(function (item) {
        if (isObject(item) || isArray(item)) {
          // first key inline after the dash
          var sub = yamlNode(item, indent + 1);
          // sub starts with newline + entries; convert first entry to dash form
          var subLines = sub.replace(/^\n/, '').split('\n');
          var firstPad = repeat('  ', indent + 1);
          subLines[0] = subLines[0].slice(firstPad.length);
          out += pad + '- ' + subLines[0] + '\n';
          for (var i = 1; i < subLines.length; i++) {
            if (subLines[i] === '') continue;
            out += pad + '  ' + subLines[i].slice(firstPad.length) + '\n';
          }
        } else {
          out += pad + '- ' + yamlScalar(item) + '\n';
        }
      });
      return out.replace(/\n$/, '');
    }
    if (isObject(v)) {
      var out2 = '\n';
      for (var k in v) {
        if (!Object.prototype.hasOwnProperty.call(v, k)) continue;
        var val = v[k];
        if (isObject(val)) {
          out2 += pad + k + ':' + yamlNode(val, indent + 1) + '\n';
        } else if (isArray(val)) {
          out2 += pad + k + ':' + yamlNode(val, indent + 1) + '\n';
        } else {
          out2 += pad + k + ': ' + yamlScalar(val) + '\n';
        }
      }
      return out2.replace(/\n$/, '');
    }
    return ' ' + yamlScalar(v);
  }

  function toYAML(spec) {
    return yamlNode(spec, 0).replace(/^\n/, '') + '\n';
  }

  /* ---------- Python literal ---------- */
  function pyLiteral(v, indent) {
    var pad = repeat('    ', indent);
    var inner = repeat('    ', indent + 1);
    if (v === null || typeof v === 'undefined') return 'None';
    if (typeof v === 'boolean') return v ? 'True' : 'False';
    if (typeof v === 'number') return String(v);
    if (typeof v === 'string') return "\"" + v.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n') + "\"";
    if (isArray(v)) {
      if (!v.length) return '[]';
      var items = v.map(function (it) { return inner + pyLiteral(it, indent + 1); });
      return '[\n' + items.join(',\n') + '\n' + pad + ']';
    }
    if (isObject(v)) {
      var keys = [];
      for (var k in v) {
        if (Object.prototype.hasOwnProperty.call(v, k)) {
          keys.push(inner + '"' + k + '": ' + pyLiteral(v[k], indent + 1));
        }
      }
      if (!keys.length) return '{}';
      return '{\n' + keys.join(',\n') + '\n' + pad + '}';
    }
    return 'None';
  }

  /* ---------- Python (config) per kind ---------- */
  function toPython(spec, kind) {
    var L = [];
    L.push('"""Generated by NSCode Build Lab — ' + kind + ' configuration."""');
    L.push('from dataclasses import dataclass, field');
    L.push('');

    if (kind === 'rag') {
      L.push('@dataclass');
      L.push('class RAGConfig:');
      L.push('    chunk_size: int = ' + (spec.chunkSize | 0));
      L.push('    overlap: int = ' + (spec.overlap | 0));
      L.push('    top_k: int = ' + (spec.topK | 0));
      L.push('    embedding_model: str = ' + pyLiteral(spec.embeddingModel || '', 0));
      L.push('    rerank: bool = ' + (spec.rerank ? 'True' : 'False'));
      L.push('    threshold: float = ' + Number(spec.threshold || 0));
      L.push('');
      L.push('');
      L.push('def build_pipeline(cfg: RAGConfig = RAGConfig()):');
      L.push('    splitter = TextSplitter(chunk_size=cfg.chunk_size, chunk_overlap=cfg.overlap)');
      L.push('    retriever = VectorRetriever(model=cfg.embedding_model, top_k=cfg.top_k, threshold=cfg.threshold)');
      L.push('    if cfg.rerank:');
      L.push('        retriever = ReRanker(retriever)');
      L.push('    return RAGPipeline(splitter=splitter, retriever=retriever)');
      return L.join('\n') + '\n';
    }

    if (kind === 'agent') {
      var tools = isArray(spec.tools) ? spec.tools : [];
      L.push('@dataclass');
      L.push('class AgentConfig:');
      L.push('    name: str = ' + pyLiteral(spec.name || '', 0));
      L.push('    goal: str = ' + pyLiteral(spec.goal || '', 0));
      L.push('    tools: list = field(default_factory=lambda: ' + pyLiteral(tools, 0) + ')');
      L.push('    max_steps: int = ' + (spec.maxSteps | 0));
      L.push('');
      L.push('');
      L.push('def build_agent(cfg: AgentConfig = AgentConfig()):');
      L.push('    return Agent(');
      L.push('        name=cfg.name,');
      L.push('        goal=cfg.goal,');
      L.push('        tools=[load_tool(t) for t in cfg.tools],');
      L.push('        max_steps=cfg.max_steps,');
      L.push('    )');
      return L.join('\n') + '\n';
    }

    if (kind === 'multi-agent') {
      var roles = isArray(spec.roles) ? spec.roles : [];
      L.push('@dataclass');
      L.push('class MultiAgentConfig:');
      L.push('    manager: str = ' + pyLiteral(spec.manager || '', 0));
      L.push('    roles: list = field(default_factory=lambda: ' + pyLiteral(roles, 0) + ')');
      L.push('    rounds: int = ' + (spec.rounds | 0));
      L.push('');
      L.push('');
      L.push('def build_team(cfg: MultiAgentConfig = MultiAgentConfig()):');
      L.push('    manager = Agent(name=cfg.manager, role="manager")');
      L.push('    workers = [Agent(name=r, role=r.lower()) for r in cfg.roles]');
      L.push('    return Crew(manager=manager, workers=workers, rounds=cfg.rounds)');
      return L.join('\n') + '\n';
    }

    if (kind === 'mcp') {
      var mtools = isArray(spec.tools) ? spec.tools : [];
      L.push('@dataclass');
      L.push('class MCPServerConfig:');
      L.push('    server_name: str = ' + pyLiteral(spec.serverName || '', 0));
      L.push('    tools: list = field(default_factory=lambda: ' + pyLiteral(mtools, 0) + ')');
      L.push('');
      L.push('');
      L.push('def build_server(cfg: MCPServerConfig = MCPServerConfig()):');
      L.push('    server = MCPServer(name=cfg.server_name)');
      L.push('    for tool in cfg.tools:');
      L.push('        @server.tool(name=tool["name"], description=tool["description"])');
      L.push('        def _handler(**kwargs):');
      L.push('            raise NotImplementedError(tool["name"])');
      L.push('    return server');
      return L.join('\n') + '\n';
    }

    if (kind === 'workflow') {
      var steps = isArray(spec.steps) ? spec.steps : [];
      L.push('@dataclass');
      L.push('class WorkflowConfig:');
      L.push('    name: str = ' + pyLiteral(spec.name || '', 0));
      L.push('    steps: list = field(default_factory=lambda: ' + pyLiteral(steps, 0) + ')');
      L.push('');
      L.push('');
      L.push('def run_workflow(cfg: WorkflowConfig = WorkflowConfig(), state=None):');
      L.push('    state = state or {}');
      L.push('    for step in cfg.steps:');
      L.push('        if step["type"] == "llm":');
      L.push('            state = call_llm(step["name"], state)');
      L.push('        elif step["type"] == "tool":');
      L.push('            state = call_tool(step["name"], state)');
      L.push('        elif step["type"] == "condition":');
      L.push('            state = evaluate(step["name"], state)');
      L.push('    return state');
      return L.join('\n') + '\n';
    }

    // fallback
    L.push('CONFIG = ' + pyLiteral(spec, 0));
    return L.join('\n') + '\n';
  }

  /* ---------- LangGraph pseudocode per kind ---------- */
  function toLangGraph(spec, kind) {
    var L = [];
    L.push('"""LangGraph-style graph generated by NSCode Build Lab — ' + kind + '."""');
    L.push('from langgraph.graph import StateGraph, START, END');
    L.push('');
    L.push('graph = StateGraph(dict)');
    L.push('');

    if (kind === 'rag') {
      L.push('# RAG retrieval-augmented generation graph');
      L.push('def retrieve(state):');
      L.push('    # top_k=' + (spec.topK | 0) + ', model=' + JSON.stringify(spec.embeddingModel || '') +
        ', threshold=' + Number(spec.threshold || 0));
      L.push('    return {"docs": vector_search(state["query"], top_k=' + (spec.topK | 0) + ')}');
      L.push('');
      if (spec.rerank) {
        L.push('def rerank(state):');
        L.push('    return {"docs": rerank_docs(state["query"], state["docs"])}');
        L.push('');
      }
      L.push('def generate(state):');
      L.push('    return {"answer": llm(state["query"], state["docs"])}');
      L.push('');
      L.push('graph.add_node("retrieve", retrieve)');
      if (spec.rerank) L.push('graph.add_node("rerank", rerank)');
      L.push('graph.add_node("generate", generate)');
      L.push('graph.add_edge(START, "retrieve")');
      if (spec.rerank) {
        L.push('graph.add_edge("retrieve", "rerank")');
        L.push('graph.add_edge("rerank", "generate")');
      } else {
        L.push('graph.add_edge("retrieve", "generate")');
      }
      L.push('graph.add_edge("generate", END)');
      L.push('');
      L.push('app = graph.compile()');
      return L.join('\n') + '\n';
    }

    if (kind === 'agent') {
      var tools = isArray(spec.tools) ? spec.tools : [];
      L.push('# Single ReAct-style agent: ' + JSON.stringify(spec.name || ''));
      L.push('# goal: ' + JSON.stringify(spec.goal || ''));
      L.push('TOOLS = ' + pyLiteral(tools, 0));
      L.push('MAX_STEPS = ' + (spec.maxSteps | 0));
      L.push('');
      L.push('def agent(state):');
      L.push('    return {"action": llm_decide(state, tools=TOOLS)}');
      L.push('');
      L.push('def act(state):');
      L.push('    return {"observation": run_tool(state["action"])}');
      L.push('');
      L.push('def should_continue(state):');
      L.push('    if state.get("step", 0) >= MAX_STEPS or state["action"] == "finish":');
      L.push('        return END');
      L.push('    return "act"');
      L.push('');
      L.push('graph.add_node("agent", agent)');
      L.push('graph.add_node("act", act)');
      L.push('graph.add_edge(START, "agent")');
      L.push('graph.add_conditional_edges("agent", should_continue, {"act": "act", END: END})');
      L.push('graph.add_edge("act", "agent")');
      L.push('');
      L.push('app = graph.compile()');
      return L.join('\n') + '\n';
    }

    if (kind === 'multi-agent') {
      var roles = isArray(spec.roles) ? spec.roles : [];
      var mgr = ident(spec.manager, 'manager');
      L.push('# Manager-orchestrated multi-agent graph');
      L.push('# manager=' + JSON.stringify(spec.manager || '') + ', rounds=' + (spec.rounds | 0));
      L.push('ROUNDS = ' + (spec.rounds | 0));
      L.push('');
      L.push('def ' + mgr + '(state):');
      L.push('    return {"plan": llm_plan(state, roles=' + pyLiteral(roles, 0) + ')}');
      L.push('');
      roles.forEach(function (r) {
        var rn = ident(r, 'worker');
        L.push('def ' + rn + '(state):');
        L.push('    return {"' + rn + '": llm_act("' + r + '", state)}');
        L.push('');
      });
      L.push('graph.add_node("' + mgr + '", ' + mgr + ')');
      roles.forEach(function (r) { L.push('graph.add_node("' + ident(r, 'worker') + '", ' + ident(r, 'worker') + ')'); });
      L.push('graph.add_edge(START, "' + mgr + '")');
      if (roles.length) {
        roles.forEach(function (r) { L.push('graph.add_edge("' + mgr + '", "' + ident(r, 'worker') + '")'); });
        roles.forEach(function (r) { L.push('graph.add_edge("' + ident(r, 'worker') + '", END)'); });
      } else {
        L.push('graph.add_edge("' + mgr + '", END)');
      }
      L.push('');
      L.push('app = graph.compile()  # loop up to ROUNDS rounds in the runtime');
      return L.join('\n') + '\n';
    }

    if (kind === 'mcp') {
      var mtools = isArray(spec.tools) ? spec.tools : [];
      L.push('# MCP server exposed as a tool node: ' + JSON.stringify(spec.serverName || ''));
      L.push('MCP_TOOLS = ' + pyLiteral(mtools, 0));
      L.push('');
      L.push('def mcp_tools(state):');
      L.push('    name = state["tool"]');
      L.push('    return {"result": mcp_call(' + JSON.stringify(spec.serverName || '') + ', name, state["args"])}');
      L.push('');
      L.push('graph.add_node("mcp_tools", mcp_tools)');
      L.push('graph.add_edge(START, "mcp_tools")');
      L.push('graph.add_edge("mcp_tools", END)');
      L.push('');
      L.push('app = graph.compile()');
      return L.join('\n') + '\n';
    }

    if (kind === 'workflow') {
      var steps = isArray(spec.steps) ? spec.steps : [];
      L.push('# Linear workflow: ' + JSON.stringify(spec.name || ''));
      if (!steps.length) {
        L.push('graph.add_edge(START, END)');
        L.push('');
        L.push('app = graph.compile()');
        return L.join('\n') + '\n';
      }
      var names = steps.map(function (s, i) { return ident(s.name, 'step_' + i); });
      // de-dup node names
      var seen = {};
      names = names.map(function (n) {
        if (seen[n]) { var j = 2; while (seen[n + '_' + j]) j++; n = n + '_' + j; }
        seen[n] = 1; return n;
      });
      steps.forEach(function (s, i) {
        L.push('def ' + names[i] + '(state):  # type=' + (s.type || 'llm'));
        if (s.type === 'tool') L.push('    return {"' + names[i] + '": run_tool("' + (s.name || '') + '", state)}');
        else if (s.type === 'condition') L.push('    return {"' + names[i] + '": evaluate("' + (s.name || '') + '", state)}');
        else L.push('    return {"' + names[i] + '": llm("' + (s.name || '') + '", state)}');
        L.push('');
      });
      names.forEach(function (n) { L.push('graph.add_node("' + n + '", ' + n + ')'); });
      L.push('graph.add_edge(START, "' + names[0] + '")');
      for (var i = 0; i < names.length - 1; i++) {
        L.push('graph.add_edge("' + names[i] + '", "' + names[i + 1] + '")');
      }
      L.push('graph.add_edge("' + names[names.length - 1] + '", END)');
      L.push('');
      L.push('app = graph.compile()');
      return L.join('\n') + '\n';
    }

    L.push('graph.add_edge(START, END)');
    L.push('app = graph.compile()');
    return L.join('\n') + '\n';
  }

  /* ---------- dispatch ---------- */
  function generate(kind, spec, format) {
    spec = spec || {};
    switch (format) {
      case 'yaml': return toYAML(spec);
      case 'python': return toPython(spec, kind);
      case 'langgraph': return toLangGraph(spec, kind);
      case 'json':
      default: return toJSON(spec);
    }
  }

  NSCode.codegen = {
    toJSON: toJSON,
    toYAML: toYAML,
    toPython: function (spec, kind) { return toPython(spec, kind); },
    toLangGraph: function (spec, kind) { return toLangGraph(spec, kind); },
    generate: generate
  };
})(window.NSCode);
